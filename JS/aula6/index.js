/* podemos usar let, var(que é mais antigo) e const para declarar variáveis*/

let nome = 'Henrique';
let nameM = 'Jessica';
const nascimentoJoao = 1984;

console.log(nome, 'nasceu em ', nascimentoJoao, '.');
console.log('Em 2000', nome, 'conheceu', nameM, '.');
console.log(nome, 'casou-se com', nameM, 'em 2012.');
console.log(nameM, 'teve 1 filho com', nome, 'em 2015');
console.log('O filho de ', nome, 'se chama Eduardo.');

